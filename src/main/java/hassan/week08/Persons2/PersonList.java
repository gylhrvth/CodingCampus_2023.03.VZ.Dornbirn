package hassan.week08.Persons2;

public class PersonList {
    private String name;
    private int age;
    private int height;
    private int weight;

    public PersonList(String name, int age, int height, int weight) {
        this.name = name;
        this.age = age;
        this.height = height;
        this.weight = weight;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public int getHeight() {
        return height;
    }

    public int getWeight() {
        return weight;
    }


}
